public class GraphicResult {
    private int strongest, weakest, average;

    public GraphicResult() {
    }

    public int getStrongest() {
        return strongest;
    }

    public void setStrongest(int strongest) {
        this.strongest = strongest;
    }

    public int getWeakest() {
        return weakest;
    }

    public void setWeakest(int weakest) {
        this.weakest = weakest;
    }

    public int getAverage() {
        return average;
    }

    public void setAverage(int average) {
        this.average = average;
    }
}
